def asignar_gen(nombre):
    if '8' in nombre:
        return '8'
    elif '9' in nombre:
        return '9'
    elif '10' in nombre:
        return '10'
    elif '11' in nombre:
        return '11'
    elif '12' in nombre:
        return '12'
    elif '13' in nombre:
        return '13'
    elif '14' in nombre:
        return '14'
    return "n/a"
